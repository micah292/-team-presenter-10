
class Employee {
    constructor(name, id, email) {
        this.name = email;
        this.id = id;
        this.email = email;

    }
    getName(){
        return this.name
    }
    getId(){
        return this.id
    }
    getEmail(){
        return this.email
    }
    getRole(){
        return "Empolyee"
    }

}




module.exports = Employee;






